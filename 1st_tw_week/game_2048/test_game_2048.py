from version_for_testing import *

def test_remove_zeros():
    assert remove_zeros([3, 1, 0, 1]) == [3, 1, 1]
    assert remove_zeros([3, 0, 0, 1]) == [3, 1]
    assert remove_zeros([3, 1, 1, 1]) == [3, 1, 1, 1]
    assert remove_zeros([1, 0, 1, 0]) == [1, 1]
    assert remove_zeros([0, 0, 0, 0]) == []

def test_left_merge():
    assert left_merge([[0,8,16,2],[0,4,8,8],[2,0,32,32],[0,0,4,0]]) == [[8,16,2,0],[4,16,0,0],[2,64,0,0],[4,0,0,0]]
    assert left_merge([[0,0,2,2],[0,0,0,64],[8,32,4,4],[2,2,16,2]]) == [[4,0,0,0],[64,0,0,0],[8,32,8,0],[4,16,2,0]]

def test_right_merge():
    assert right_merge([[0,0,0,2],[4,2,0,0],[32,16,2,0],[16,4,8,0]])==[[0,0,0,2],[0,0,4,2],[0,32,16,2],[0,16,4,8]]

def test_up_merge():
    assert up_merge([[8,0,0,2],[4,0,0,0],[2,32,0,0],[4,64,2,2]])==[[8,32,2,4],[4,64,0,0],[2,0,0,0],[4,0,0,0]]
    assert up_merge([[0,0,0,4],[0,0,4,8],[2,16,32,4],[4,8,64,4]])==[[2,16,4,4],[4,8,32,8],[0,0,64,8],[0,0,0,0]]

def test_down_merge():
    assert down_merge([[0,0,2,2],[0,0,4,2],[0,32,16,2],[0,16,4,8]]) == [[0,0,2,0],[0,0,4,2],[0,32,16,4],[0,16,4,8]]
    assert down_merge([[8,16,2,0],[4,16,0,2],[2,64,0,0],[4,0,0,0]]) == [[8,0,0,0],[4,0,0,0],[2,32,0,0],[4,64,2,2]]
    assert down_merge([[16, 8, 2, 0], [32, 8, 0, 0], [64, 0, 2, 0],[16,8,8,0]]) == [[16,0,0,0],[32,0,0,0],[64,8,4,0],[16,16,8,0]]

def test_merge_line():
    assert merge_line([0, 0, 0, 0]) == [0, 0, 0, 0]
    assert merge_line([2, 0, 2, 2]) == [4, 2, 0, 0]
    assert merge_line([2, 2, 2, 2]) == [4, 4, 0, 0]
    assert merge_line([0, 0, 2, 2]) == [4, 0, 0, 0]
    assert merge_line([0, 0, 0, 4]) == [4, 0, 0, 0]
    assert merge_line([2, 0, 4, 2]) == [2, 4, 2, 0]
    assert merge_line([2, 2, 8, 4]) == [4, 8, 4, 0]
    assert merge_line([2, 2, 2, 8]) == [4, 2, 8, 0]
    assert merge_line([2, 0, 0, 2]) == [4, 0, 0, 0]